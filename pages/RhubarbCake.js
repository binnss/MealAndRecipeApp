                '• 1 tsp lemon juice\n' +
                '• 1/2 cup unsalted butter at room temperature\n' +
                '• 1 cup granulated sugar plus 2 tsp for sprinkling\n' +
                '• 1 large egg, at room temperature\n' +
                '• 1 1/2 tsp vanilla extract\n' +
                '• 2 cups all purpose flour, saving out 1 Tbsp to toss with rhubarb\n' +
                '• 2 tsp baking powder\n' +
                '• 1 tsp salt\n' +
                '• 2 cups thinly sliced rhubarb\n' ;
